namespace UnitTests
{
    public class SimpleArithmetic
    {
        public decimal Addition(int x, int y)
        {
            return x + y;
        }

        public decimal Subtraction(int x, int y)
        {
            return x - y;
        }

        public decimal Multiplication(int x, int y)
        {
            return x * y;
        }

        public float Division(float x, float y)
        {
            return x / y;
        }
    }
}